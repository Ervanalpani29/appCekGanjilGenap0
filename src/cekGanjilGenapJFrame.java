    private void angkaFieldActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_angkaFieldActionPerformed
 try {
        // Mendapatkan input dari angkaField dan mengonversinya ke tipe integer
        int angka = Integer.parseInt(angkaField.getText());
        
        // Mengecek apakah angka genap atau ganjil, dan mengubah teks resultLabel sesuai hasil
        if (angka % 2 == 0) {
            jLabel1.setText("Angka " + angka + " adalah Genap.");
        } else {
            jLabel1.setText("Angka " + angka + " adalah Ganjil.");
        }
    } catch (NumberFormatException e) {
        // Menangani kasus input yang bukan angka
            jLabel1.setText("Masukkan angka yang valid.");
    }
    }//GEN-LAST:event_angkaFieldActionPerformed


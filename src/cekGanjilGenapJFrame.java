    private void genaptFieldKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_genaptFieldKeyPressed
         genaptField.setText("");
    
    // Cek apakah tombol yang ditekan adalah Enter (kode 10)
    if (evt.getKeyCode() ==  KeyEvent.VK_ENTER) {
        try {
            int angka = Integer.parseInt(genaptField.getText());

            // Mengecek apakah angka genap atau ganjil
            if (angka % 2 == 0) {
                 genaptField.setText(angka + " adalah angka genap.");
            } else {
                 genaptField.setText(angka + " adalah angka ganjil.");
            }
        } catch (NumberFormatException e) {
            // Menangani jika input bukan angka
             genaptField.setText("Masukkan angka yang valid.");
        }
    }
    }//GEN-LAST:event_genaptFieldKeyPressed

